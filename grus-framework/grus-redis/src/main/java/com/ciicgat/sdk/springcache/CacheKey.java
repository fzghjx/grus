package com.ciicgat.sdk.springcache;

/**
 * @Author: August
 * @Date: 2021/4/15 9:47
 */
public interface CacheKey {
    String cacheKey();

    static String key(Object key) {
        return key instanceof CacheKey ? ((CacheKey) key).cacheKey() : key.toString();
    }
}
